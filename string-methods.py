var_str = 'hello'
# Return variable data type
print(type(var_str))

# Uppercase
print(var_str.upper())

# Capital case
print(var_str.capitalize())

# Count a word in a sentence *case sensitive
print(var_str.count('l'))

# String template literal
literal_string = f'Hello, my age is {8 + 9} years old'
print(literal_string)