x = 10
y = 12

# Add
print(x + y)

# Subtract
print(y - x)

# Multiply
print(x * y)

# Divide
print(y / x)

# Power
print(x ** 2)

# Divide with floor (doesn't return a decimal when divided)
print(23 // 3)

# Input Example
var_x2 = input('Please input your number that will multiplied by 2: ')
print(int(var_x2) * 2)
